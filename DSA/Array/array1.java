package DSA.Array;

public class array1 {
    public static void main(String[] args) {
        System.out.println("Array Elements:");

        int[] arr = { 1, 2, 3, 4, 5 };
        for (int i = 0; i < arr.length; i++) {
            System.out.println("Element: " + arr[i] + ", Index: " + i);
        }
    }
}
